// indexOf(): Takes takes a substring and if the substring exists in a string it returns the first position of the substring if does not exist it returns -1

// string.indexOf(substring)

let string = 'I am FullStack Developer'
console.log(string.indexOf('m'))          // 3
console.log(string.indexOf('FullStack'))       // 5
console.log(string.indexOf('days'))       // -1
console.log(string.indexOf('a'))          // 2
console.log(string.indexOf('Developer')) // 15
console.log(string.indexOf('script'))     // -1
